// Resolve each polygon against the registry: colour + label anchor (centroid)
// are owned by lib/zones, geometry is owned here.
export const ZONE_SHAPES: ZoneShape[] = (Object.keys(ZONE_POLYGONS) as ZoneCode[]).map((code) => {
  const meta = ZONES[code];
  const labelAt = meta.centroid;
  if (!labelAt) throw new Error(`Zone "${code}" has a map polygon but no centroid in lib/zones`);
  return { code, color: meta.color, labelAt, poly: ZONE_POLYGONS[code]! };
});
